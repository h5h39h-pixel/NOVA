# -*- coding: utf-8 -*-
"""OUT-3 — image generation verification. Trigger a real image generation through the live API
(ComfyUI via TOOLKIT/generate.ps1) and confirm a valid, non-trivial PNG is actually produced — not
just that the job started. Writes docs/generation-baseline.md.

Outcome verification, not a unit test. Requires the server (:8900) and ComfyUI (:8188) running with
an SDXL/Flux checkpoint. Usage: python scripts/gen_eval.py [--model sdxl] [--timeout 180] [--write-doc]
"""
import argparse
import json
import sys
import time
import urllib.request
from datetime import datetime, timezone
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
if str(ROOT) not in sys.path:
    sys.path.insert(0, str(ROOT))
from config import UPLOAD_DIR, COMFY_URL  # noqa: E402

BASE = "http://127.0.0.1:8900"


def _post(path, body):
    data = json.dumps(body).encode()
    rq = urllib.request.Request(BASE + path, data=data, headers={"Content-Type": "application/json"}, method="POST")
    with urllib.request.urlopen(rq, timeout=30) as r:
        return json.loads(r.read().decode())


def _up(url):
    try:
        with urllib.request.urlopen(url, timeout=4) as r:
            return r.status == 200
    except Exception:
        return False


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--model", default="sdxl")
    ap.add_argument("--timeout", type=int, default=180)
    ap.add_argument("--write-doc", action="store_true")
    args = ap.parse_args()

    if not _up(BASE + "/api/health"):
        print("ERROR: control center not running on :8900"); return 1
    if not _up(COMFY_URL + "/system_stats"):
        print("ERROR: ComfyUI not running on", COMFY_URL); return 1

    prompt = "a single red apple on a white table, studio photo"
    print(f"Generating ({args.model}): {prompt!r}")
    t0 = time.time()
    r = _post("/api/toolkit/image", {"prompt": prompt, "model": args.model})
    fn = (r.get("file") or "").replace("/files/", "")
    if not fn:
        print("ERROR: no file returned:", r); return 1
    out = UPLOAD_DIR / fn
    print("job:", r.get("job"), "-> waiting for", out)

    ok = False
    w = h = size = 0
    while time.time() - t0 < args.timeout:
        if out.exists() and out.stat().st_size > 1024:
            time.sleep(1.0)  # let the writer finish
            try:
                from PIL import Image
                im = Image.open(out); im.load(); w, h = im.size
                size = out.stat().st_size
                ok = w >= 64 and h >= 64
                break
            except Exception:
                pass
        time.sleep(2)
    dt = round(time.time() - t0, 1)

    if ok:
        print(f"[PASS] produced {fn}: {w}x{h}, {size//1024} KB in {dt}s")
    else:
        print(f"[FAIL] no valid image after {dt}s (file={out}, exists={out.exists()})")

    if args.write_doc:
        ts = datetime.now(timezone.utc).astimezone().strftime("%Y-%m-%d %H:%M %Z")
        lines = [
            "# Image generation baseline (OUT-3)", "",
            f"_Generated by `scripts/gen_eval.py` on {ts}._", "",
            "Triggers a real image generation through the live API (ComfyUI) and verifies a valid PNG is "
            "produced — outcome verification, not a unit test.", "",
            f"**Result: {'✅ PASS' if ok else '❌ FAIL'}** — model `{args.model}`, "
            + (f"{w}×{h}px, {size//1024} KB, {dt}s." if ok else f"no valid image after {dt}s."), "",
            "| Field | Value |", "|---|---|",
            f"| Model | {args.model} |", f"| Prompt | {prompt} |",
            f"| Output | {fn if ok else '(none)'} |", f"| Dimensions | {w}×{h} |",
            f"| Size | {size//1024} KB |", f"| Time | {dt}s |", "",
            "Re-run: `python scripts/gen_eval.py --write-doc`. Video: the same path drives "
            "`/api/toolkit/video` (LTX) — slower; verify manually for now.", "",
        ]
        (ROOT / "docs" / "generation-baseline.md").write_text("\n".join(lines), encoding="utf-8")
        print("wrote docs/generation-baseline.md")
    return 0 if ok else 1


if __name__ == "__main__":
    sys.exit(main())
