# -*- coding: utf-8 -*-
"""OUT-2 — verify the trained `nova-local` model is usable end-to-end. Runs identity / knowledge /
bilingual prompts through the model and checks the responses are coherent and reflect its training
(knows it's Nova, the RTX 5090, the Control Center; answers Arabic in Arabic). Writes
docs/nova-local-eval.md.

This verifies the *output* of the training pipeline (the part we can check here). The fine-tune
*process/quality* itself is owner-run on C:\\AI\\training — see docs/training-pipeline.md.

Usage: python scripts/nova_eval.py [--model nova-local] [--write-doc]
"""
import argparse
import json
import re
import sys
import urllib.request
from datetime import datetime, timezone
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
if str(ROOT) not in sys.path:
    sys.path.insert(0, str(ROOT))
from config import OLLAMA  # noqa: E402

# each check: prompt + a predicate over the lowercased reply
CHECKS = [
    ("Who are you? Answer in one sentence.",
     lambda r: "nova" in r, "identifies as Nova"),
    ("What GPU is in this PC?",
     lambda r: "5090" in r or "rtx" in r, "knows the RTX 5090"),
    ("In one sentence, what is the AI Control Center?",
     lambda r: any(w in r for w in ("control", "local", "ai", "dashboard", "assistant")), "knows its domain"),
    ("اكتب جملة واحدة تعرّف فيها بنفسك.",
     lambda r: bool(re.search(r"[؀-ۿ]", r)), "responds in Arabic (bilingual)"),
]


def ask(model, prompt, num_predict=120):
    body = json.dumps({"model": model, "prompt": prompt, "stream": False,
                       "options": {"num_predict": num_predict, "temperature": 0.3}}).encode()
    rq = urllib.request.Request(OLLAMA + "/api/generate", data=body,
                                headers={"Content-Type": "application/json"}, method="POST")
    with urllib.request.urlopen(rq, timeout=300) as r:
        return (json.loads(r.read().decode()).get("response") or "").strip()


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--model", default="nova-local")
    ap.add_argument("--write-doc", action="store_true")
    args = ap.parse_args()

    print(f"Evaluating {args.model} ({len(CHECKS)} checks)…\n")
    rows, passed = [], 0
    for prompt, pred, desc in CHECKS:
        try:
            reply = ask(args.model, prompt)
        except Exception as e:
            print("ERROR:", e); return 1
        ok = bool(reply) and pred(reply.lower())
        passed += 1 if ok else 0
        rows.append((desc, ok, reply))
        print(f"[{'PASS' if ok else 'FAIL'}] {desc}\n   → {reply[:140]}")

    rate = round(100 * passed / len(CHECKS))
    print(f"\n=== {args.model}: {passed}/{len(CHECKS)} ({rate}%) usability checks passed ===")

    if args.write_doc:
        ts = datetime.now(timezone.utc).astimezone().strftime("%Y-%m-%d %H:%M %Z")
        lines = [
            "# Trained model usability check (OUT-2)", "",
            f"_Generated by `scripts/nova_eval.py` on {ts} — model **{args.model}**._", "",
            "Verifies the *output* of the LoRA training pipeline (`docs/training-pipeline.md`) is a usable "
            "model: identity, domain knowledge, and bilingual response. The fine-tune *quality* itself is "
            "owner-run on the external training box.", "",
            f"**Result: {passed}/{len(CHECKS)} ({rate}%) checks passed.**", "",
            "| Check | Pass | Reply (truncated) |", "|---|---|---|",
        ]
        for desc, ok, reply in rows:
            lines.append(f"| {desc} | {'✅' if ok else '❌'} | {reply[:120].replace(chr(124), '/').replace(chr(10), ' ')} |")
        lines += ["", "Re-run: `python scripts/nova_eval.py --write-doc`.", ""]
        (ROOT / "docs" / "nova-local-eval.md").write_text("\n".join(lines), encoding="utf-8")
        print("wrote docs/nova-local-eval.md")
    return 0 if passed >= len(CHECKS) - 1 else 1   # tolerate one soft miss


if __name__ == "__main__":
    sys.exit(main())
